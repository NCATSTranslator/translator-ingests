"""End-to-end Koza integration test for the STRING ingest.

Runs the full Koza pipeline (reader → transform → writer) against a small
200-row gzipped slice of the real human ``protein.links.v12.0`` file checked
into the repo, and asserts on the resulting JSONL output. This exercises:

  - the reader yaml (space delimiter, header on row 0, gzip detection)
  - the @koza.transform_record function (parse_string_protein_id,
    passes_combined_score, sorted_pair_key, edge construction)
  - the writer (JSONL output, KGX-compatible node/edge structure)

The unit tests in ``tests/unit/ingests/string/test_string.py`` cover the
helpers and transform in isolation. This integration test catches reader-yaml
mismatches, ID format quirks, and writer-config issues that the unit tests
can't see.
"""

import gzip
import json
from pathlib import Path

import pytest
from koza.runner import KozaRunner

from tests.util import find_project_root, get_ingest_config_yaml_path


# Directory holding both fixtures: the 200-row gzipped link file (generated by
# ``gunzip -c data/9606.protein.links.v12.0.txt.gz | head -200 | gzip > ...``)
# and a small entrez_2_string.tsv that maps 18 of the 21 above-threshold
# proteins to fake NCBIGene CURIEs. Koza resolves auxiliary files via
# ``koza.input_files_dir``, so the entrez mapping must live alongside the link
# fixture in this directory.
FIXTURE_DIR = (
    find_project_root()
    / "tests"
    / "unit"
    / "ingests"
    / "string"
    / "sample_data"
)
ENTREZ_FIXTURE = FIXTURE_DIR / "all_organisms.entrez_2_string.tsv"
# Per-taxon fixture filenames (must match string.yaml's reader configuration).
# PPI uses the ``.full.`` 16-column variant for per-channel edge selection.
PPI_FIXTURES = [
    FIXTURE_DIR / "9606.protein.links.full.v12.0.txt.gz",
    FIXTURE_DIR / "10090.protein.links.full.v12.0.txt.gz",
    FIXTURE_DIR / "10116.protein.links.full.v12.0.txt.gz",
]
PPI_HEADER = [
    "protein1", "protein2",
    "neighborhood", "neighborhood_transferred",
    "fusion", "cooccurence", "homology",
    "coexpression", "coexpression_transferred",
    "experiments", "experiments_transferred",
    "database", "database_transferred",
    "textmining", "textmining_transferred",
    "combined_score",
]


@pytest.fixture(scope="module")
def koza_output(tmp_path_factory) -> Path:
    """
    Run the full Koza pipeline (``string_ppi`` reader, all three taxa) once per
    module against the checked-in fixtures and return the output dir.
    """
    for f in [ENTREZ_FIXTURE, *PPI_FIXTURES]:
        assert f.exists(), f"Missing fixture: {f}"
    output_dir = tmp_path_factory.mktemp("string_output")

    config_yaml_path = get_ingest_config_yaml_path("string")
    assert config_yaml_path is not None, "string.yaml not found"

    # Our fixture filenames match the yaml's `files:` declarations exactly, so
    # we don't need to override `input_files` — Koza picks them up by name from
    # the yaml and resolves them under input_files_dir.
    _, runner = KozaRunner.from_config_file(
        str(config_yaml_path),
        output_dir=str(output_dir),
        input_files_dir=str(FIXTURE_DIR),
        output_format="jsonl",
    )
    runner.run()
    return output_dir


def _load_all(output_dir: Path) -> tuple[list[dict], list[dict]]:
    """Load all nodes and edges from the merged output files."""
    nodes = _load_jsonl(output_dir / "string_nodes.jsonl")
    edges = _load_jsonl(output_dir / "string_edges.jsonl")
    return nodes, edges


def _ppi_partition(nodes: list[dict], edges: list[dict]) -> tuple[list[dict], list[dict]]:
    """Filter the merged output to just the STRING PPI subset."""
    ppi_edges = [
        e for e in edges
        if any(
            s["resource_id"] == "infores:string" and s["resource_role"] == "primary_knowledge_source"
            for s in e["sources"]
        )
    ]
    ppi_node_ids = {e["subject"] for e in ppi_edges} | {e["object"] for e in ppi_edges}
    ppi_nodes = [n for n in nodes if n["id"] in ppi_node_ids]
    return ppi_nodes, ppi_edges


def _load_jsonl(path: Path) -> list[dict]:
    with open(path) as fh:
        return [json.loads(line) for line in fh if line.strip()]


@pytest.mark.parametrize(
    "fixture_name,expected_rows,expected_header,delimiter",
    [
        ("9606.protein.links.full.v12.0.txt.gz",     200,  PPI_HEADER, None),
        ("10090.protein.links.full.v12.0.txt.gz",    200,  PPI_HEADER, None),
        ("10116.protein.links.full.v12.0.txt.gz",    200,  PPI_HEADER, None),
    ],
)
def test_fixture_row_counts(fixture_name, expected_rows, expected_header, delimiter):
    """Sanity-check each fixture: row count and header."""
    path = FIXTURE_DIR / fixture_name
    with gzip.open(path, "rt") as fh:
        lines = fh.readlines()
    assert len(lines) == expected_rows, f"{fixture_name}: expected {expected_rows} rows, got {len(lines)}"
    actual_header = lines[0].strip().split(delimiter) if delimiter else lines[0].strip().split()
    assert actual_header == expected_header, f"{fixture_name}: header mismatch"


# ──── string_ppi tag: protein-protein interaction assertions ─────────────────


EXPECTED_TAXA = {"NCBITaxon:9606", "NCBITaxon:10090", "NCBITaxon:10116"}
ENSEMBL_PROTEIN_PREFIXES = ("ENSEMBL:ENSP", "ENSEMBL:ENSMUSP", "ENSEMBL:ENSRNOP")

# Edge types that may appear in PPI output under the 2026-07-17 edge model.
PPI_PREDICATES = {
    "biolink:associated_with",                    # always-emitted functional association
    "biolink:directly_physically_interacts_with", # experiments channel
    "biolink:coexpressed_with",                   # coexpression channel
}

# Association classes that may appear per predicate.
# associated_with → Association (base class)
# directly_physically_interacts_with → PairwiseMolecularInteraction
# coexpressed_with → GeneToGeneCoexpressionAssociation
PPI_CATEGORIES = {
    "biolink:Association",
    "biolink:PairwiseMolecularInteraction",
    "biolink:GeneToGeneCoexpressionAssociation",
}

PPI_KNOWLEDGE_LEVELS = {
    "knowledge_assertion",
    "statistical_association",
}
PPI_AGENT_TYPES = {
    "data_analysis_pipeline",
}


def test_ppi_produces_nodes_and_edges(koza_output):
    nodes, edges = _ppi_partition(*_load_all(koza_output))
    # 199 data rows × 3 species, with combined_score > 700 filter: expect
    # fewer edges than the old threshold=500 run. Range is loose.
    assert len(edges) > 0, f"no PPI edges produced"
    assert len(nodes) > 0, f"no PPI nodes produced"


def test_ppi_always_predicate_present(koza_output):
    """Every row above the threshold emits at least one associated_with edge."""
    _, edges = _ppi_partition(*_load_all(koza_output))
    assert any(e["predicate"] == "biolink:associated_with" for e in edges), \
        "no associated_with edges found"


def test_ppi_node_shape(koza_output):
    nodes, _ = _ppi_partition(*_load_all(koza_output))
    seen_taxa = set()
    for node in nodes:
        assert node["id"].startswith(ENSEMBL_PROTEIN_PREFIXES), node["id"]
        assert node["category"] == ["biolink:Protein"]
        assert len(node["in_taxon"]) == 1 and node["in_taxon"][0] in EXPECTED_TAXA, node["in_taxon"]
        seen_taxa.add(node["in_taxon"][0])
    # All three taxa should appear at least once in the output.
    assert seen_taxa == EXPECTED_TAXA, f"missing taxa: {EXPECTED_TAXA - seen_taxa}"


def test_ppi_equivalent_identifiers_populated_from_mapping_fixture(koza_output):
    """The fixture mapping covers 18 of 21 distinct proteins above threshold.
    Verify most PPI nodes carry NCBIGene equivalents."""
    nodes, _ = _ppi_partition(*_load_all(koza_output))
    with_equivs = [n for n in nodes if n.get("equivalent_identifiers")]
    assert len(with_equivs) > 0, "no nodes got equivalent_identifiers — mapping load failed?"
    for node in with_equivs:
        for eq in node["equivalent_identifiers"]:
            assert eq.startswith("NCBIGene:"), eq


def test_ppi_edge_shape(koza_output):
    _, edges = _ppi_partition(*_load_all(koza_output))
    for edge in edges:
        assert edge["predicate"] in PPI_PREDICATES, edge["predicate"]
        assert edge["subject"].startswith(ENSEMBL_PROTEIN_PREFIXES)
        assert edge["object"].startswith(ENSEMBL_PROTEIN_PREFIXES)
        assert edge["knowledge_level"] in PPI_KNOWLEDGE_LEVELS, edge["knowledge_level"]
        assert edge["agent_type"] in PPI_AGENT_TYPES, edge["agent_type"]


def test_ppi_no_duplicate_edges_by_pair_and_predicate(koza_output):
    """Symmetric duplicate rows in STRING should be collapsed to one edge per predicate."""
    _, edges = _ppi_partition(*_load_all(koza_output))
    pair_predicate_keys = {
        (tuple(sorted([e["subject"], e["object"]])), e["predicate"])
        for e in edges
    }
    assert len(pair_predicate_keys) == len(edges), \
        "symmetric duplicates or duplicate (pair, predicate) leaked into PPI output"


def test_ppi_knowledge_level_varies_by_edge_type(koza_output):
    """associated_with uses knowledge_assertion; coexpressed_with uses
    statistical_association — so the output should contain both."""
    _, edges = _ppi_partition(*_load_all(koza_output))
    kls = {e["knowledge_level"] for e in edges}
    # If coexpressed_with edges are present (coexpression channel fires in fixtures)
    # we expect statistical_association to appear. Only assert knowledge_assertion
    # since associated_with always fires.
    assert "knowledge_assertion" in kls, f"knowledge_assertion missing: {kls}"


# ──── output-wide assertions ─────────────────────────────────────────────────


def test_all_edges_are_string_sourced(koza_output):
    """Every emitted edge has infores:string as its primary knowledge source."""
    nodes, edges = _load_all(koza_output)
    ppi_nodes, ppi_edges = _ppi_partition(nodes, edges)
    assert len(ppi_edges) == len(edges), "an edge is not infores:string-sourced"


def test_no_duplicate_nodes_in_output(koza_output):
    """Each node id appears at most once across the output."""
    nodes, _ = _load_all(koza_output)
    ids = [n["id"] for n in nodes]
    assert len(ids) == len(set(ids)), "duplicate node IDs in output"
